package com.hxhy.model.dto;

public class Employee {
	
	private Long id;//
	private Long department_id;
	private String user_id;
	private String position;//职位
	private String name;//姓名
	private Integer is_work;//是否在职
	private String work_name;
	private Double basics_money;//基本工资
	private Double money;//实际工资
	private Integer is_attendance;//是否参与考勤;
	private Integer is_settlement;
	private Integer word_id;
	private String department;//部门
	private String title;//标题（国家规定上班时间）
	private String description;//周一到周五 
	
	public Long getId() {
		return id;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public Long getDepartment_id() {
		return department_id;
	}
	public void setDepartment_id(Long department_id) {
		this.department_id = department_id;
	}
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPosition() {
		return position;
	}
	public void setPosition(String position) {
		this.position = position;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Integer getIs_work() {
		return is_work;
	}
	public void setIs_work(Integer is_work) {
		if(is_work != null) {
			this.is_work = is_work;
			if(is_work == 1) {
				this.work_name = "在职";
			}else {
				this.work_name = "离职";
			}
		}
	}
	public String getWork_name() {
		return work_name;
	}
	public void setWork_name(String work_name) {
		this.work_name = work_name;
	}
	public Double getBasics_money() {
		return basics_money;
	}
	public void setBasics_money(Double basics_money) {
		this.basics_money = basics_money;
	}
	public Double getMoney() {
		return money;
	}
	public void setMoney(Double money) {
		this.money = money;
	}
	public Integer getIs_attendance() {
		return is_attendance;
	}
	public void setIs_attendance(Integer is_attendance) {
		this.is_attendance = is_attendance;
	}
	public Integer getIs_settlement() {
		return is_settlement;
	}
	public void setIs_settlement(Integer is_settlement) {
		this.is_settlement = is_settlement;
	}
	public Integer getWord_id() {
		return word_id;
	}
	public void setWord_id(Integer word_id) {
		this.word_id = word_id;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}

}

