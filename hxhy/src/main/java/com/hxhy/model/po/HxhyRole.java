package com.hxhy.model.po;

import com.hxhy.model.common.BaseEntity;

public class HxhyRole extends BaseEntity {
	
	private String title;//名称
	private String description;//描述
	private String power;//权限
	private Integer is_del;//是否可以删除
	private Integer is_change;//是否可以修改
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}
	public String getPower() {
		return power;
	}
	public void setPower(String power) {
		this.power = power;
	}
	public Integer getIs_del() {
		return is_del;
	}
	public void setIs_del(Integer is_del) {
		this.is_del = is_del;
	}
	public Integer getIs_change() {
		return is_change;
	}
	public void setIs_change(Integer is_change) {
		this.is_change = is_change;
	}
}
